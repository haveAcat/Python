#
#   字元資料的轉換
#
#   1. chr(x) : 可以傳回 x 值字元對應的 ASCII code 碼值
#
#   2. ord(x) : 可以傳回 x 值字元對應的 Unicode 碼值
#

x1 = 97

x2 = chr(x1)      

print(x2)             # 輸出數值97的字元

#====================================================

x3 = ord(x2)

print(x3)             # 輸出字元x3的Unicode碼值

#====================================================

x4 = '魁'

print(ord(x4))        # 輸出字元'魁'的Unicode碼值

