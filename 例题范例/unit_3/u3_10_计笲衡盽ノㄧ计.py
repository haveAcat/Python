#
#   數值運算常用到的函數
#
#   1. abs() : 計算絕對值
#
#   2. pow(x, y) : 計算 x 的 y 次方
#
#   3. round() : 計算小數點的進位近似值，採用的是"5捨6入" 法。
#

x = -10

print("以下輸出abs( )函數的應用")

print(x)            # 輸出x變數

print(abs(x))       # 輸出abs(x)

#====================================

x = 5

y = 3

print("以下輸出pow( )函數的應用")

print(pow(x, y))    # 輸出pow(x,y)

#====================================

